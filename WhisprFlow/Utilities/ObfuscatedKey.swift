    /// Utility to generate encoded bytes from a key (for development only)
    /// Call this with your API key to generate the encoded array above
    #if DEBUG
    static func encode(_ key: String) -> String {
        let xorKey: [UInt8] = [
            0x7A, 0x4B, 0x9C, 0x2D, 0xE1, 0x5F, 0x83, 0xB6,
            0x1A, 0xC4, 0x67, 0xD8, 0x3E, 0x91, 0xF5, 0x2C
        ]
        
        let keyBytes = Array(key.utf8)
        var encoded = [UInt8]()
        
        for (index, byte) in keyBytes.enumerated() {
            let xorByte = xorKey[index % xorKey.count]
            encoded.append(byte ^ xorByte)
        }
        
        // Format as Swift array literal
        var result = "let encoded: [UInt8] = [\n"
        for (index, byte) in encoded.enumerated() {
            if index % 8 == 0 {
                result += "    "
            }
            result += String(format: "0x%02x", byte)
            if index < encoded.count - 1 {
                result += ", "
            }
            if index % 8 == 7 {
                result += "\n"
            }
        }
        result += "\n]"
        return result
    }
